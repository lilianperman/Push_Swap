#include "libft.h"

int	push_isdigit(char *arg)
{
	int	i;

	i = 0;
	while (arg[i])
	{
		if ((arg[0] == '-' || arg[0] == '+') && i == 0)
				i++;
		if (!(arg[i] >= '0' && arg[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
